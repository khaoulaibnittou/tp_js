/**
 *
 *
 * @param {number} a premier nombre
 * @param {number} b deuxieme nombre
 * @return {number} resultat
 */
function somme(a, b) {
    return a + b;
}

console.log(somme(5, 3));  
console.log(somme(-2, 7));  
console.log(somme(0, 0));   
console.log(somme(-5, -10)); 
console.log(somme(10, 0));  
